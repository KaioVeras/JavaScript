// Trabalhando com loop

// While = enquanto

let x = 0;

document.write('<br><br> ============ While ============ <br>');

while (x <= 20) {
  document.write(`<br> O  X é o número: ${x}`);
  x++;
}

// For = para

document.write('<br><br> ============ For ============ <br>');

for(i = 0; i <= 20; i++){
    document.write(`<br> O I é o número: ${i}`);
}